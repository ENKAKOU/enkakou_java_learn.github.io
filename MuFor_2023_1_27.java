import java.util.Scanner;

public class MuFor_2023_1_27 {
	
    public static void main(String[] args){
    	
        for(int a = 0; a < 2; a++){
            for(int g = 0; g < 3; g++){
                System.out.println("a=" + a + "g=" + g);
            }
        }
        /* 习题1：统计3个班级的成绩情况，每个班有5名同学
                 求各个班级平均分和所有班级的平均分（学生的成绩从键盘输入）
                 统计三个班及格人数，每个班有5名同学
            /*思路分析（化繁为简）；
                 1. 先计算一个班，5个学生的成绩;使用for循环;
                    创建Scanner对象，接收用户输入;
                    得到该班级的平均分，先定义一个 double sum 把该班级5个学生的成绩累计起来
                 2. 统计3个班级（每个班5个学生）平均分
                 3. 所有班级的平均分
                    定义一个变量 double totalScore 累计所有学生的成绩
                    当多重循环结束后，totalScore / (3 * 5);
                 4. 统计三个班及格人数
                    定义变量 int passNum = 0; 当有一个学生成绩 >= 60, passNum++
                    如果>=60  passNum++
                 5. 优化（效率，可读性，结构）*/

        Scanner myScanner = new Scanner(System.in);
        double totalScore = 0;  //累计所有学生的成绩
        int passNum = 0;   //累计 及格人数
        for(int i = 1; i <= 3; i++){    // i 表示班级
            double sum = 0;     // 一个班级的总分
            for(int j = 1; j <= 5; j++){    // j 表示学生
              System.out.println("请输入第" + i + "班的第" + j +"个学生的成绩");
              double score = myScanner.nextDouble();
                //当有一个学生成绩 >= 60, passNum++
              if(score >= 60){
                  passNum++;
            }
            sum += score;   // 累计
            System.out.println("成绩为" + score);
        }
            //因为sum 是 5个学生的总成绩
        System.out.println("sum=" + sum + " 平均分=" + (sum / 5));
            //把sum 累计到 totalScore
            totalScore += sum;
        }
        System.out.println("三个班总分=" + totalScore + " 平均分=" + totalScore / 15);
        System.out.println("及格人数有=" + passNum);


        // 打印一个矩形
        for(int y = 1; y <= 5; y++){   //y 表示层数
            System.out.println("*****");
        }
        // 打印半个金字塔
        for(int w = 1; w <= 5; w++){
            // 控制打印每层的 * 个
            for( int v = 1; v <= w; v++){
                System.out.println("*");
        }
            System.out.println("");
        }



    }
}
